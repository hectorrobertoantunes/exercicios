# Conversor de medidas

medida = float(input('Coloque aqui o valor em metros (S.I): '))
print('Essa medida em kilômetros é {} km'.format(medida/1000))
print('Essa medida em hectômetro é {} hm'.format(medida/100))
print('Essa medida em dacâmetro é {} dam'.format(medida/10))
print('Essa medida em metros é de {} m'.format(medida))
print('Essa medida em decímetros é de {} dm'.format(medida*10))
print('Essa medida em centímetros é de {} cm'.format(medida*100))
print('Essa medida em milímetros é de {} mm'.format(medida*1000))